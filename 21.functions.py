# functions --> a block of code which is executed only when it is called...

def repeat():
    print("I will not repeat my code")
    print("Have a nice day")
    
repeat()


def hello(name): # function with parameter
    print("Hello " + name)
    print("good morning")
    
hello("Tom")


def greeting(first_name,last_name,age):
    print("Hello "+ first_name + " " + last_name)
    print("You are "+ str(age) + " years old")
    
greeting("Cartoon","Network",44)


