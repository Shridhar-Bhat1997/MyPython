import math
pi=3.14

print(round(pi)) # returns a floating-point number rounded to the specified number of decimals.
print(math.ceil(pi)) # rounds a number UP to the nearest integer.
print(math.floor(pi)) # rounds a number DOWN to the nearest integer.
print(abs(pi)) # returns the absolute value of the given number. ie -10 is also 10.
print(pow(3,2)) # returns the value of x to the power of y.
print(math.sqrt(49)) # returns square root of any number.

